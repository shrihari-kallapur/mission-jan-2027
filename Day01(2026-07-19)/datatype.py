# Integer
age = 27
print("Age: ", age)

# Float
cgpa = 8.55
print("CGPA: ", cgpa)

# String
college = "PES University"
print("College: ", college)

# Boolean
is_graduating = True
print("Is Graduating: ", is_graduating)

# Checking Types
print("Type of age:", type(age))
print("Type of cgpa:", type(cgpa))
print("Type of college:", type(college))
print("Type of is_graduating:", type(is_graduating))
